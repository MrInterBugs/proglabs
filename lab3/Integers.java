class Integers {
	public static void main(String[] args) {
		for (int i=0; i<20; i=i+2) {
    			System.out.println(i);
		}
		for (int i=1; i<=21; i=i+2) {
			System.out.println(i);
		}
		for (int i=5; i<=100; i=i+5) {
			System.out.println(i);
		}
		for (int i=20; i>0; i--) {
			System.out.println(i);
		}
	}
}
